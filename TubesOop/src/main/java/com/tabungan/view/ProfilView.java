   private void tampilkanProfil() {
    ProfilController profilController = new ProfilController();
    Profil profil = profilController.ambilProfil(username);

    if (profil != null) {
        txtNama.setText(profil.getNama());
        txtEmail.setText(profil.getEmail());
        cmbGender.setSelectedItem(profil.getGender());
    } else {
        JOptionPane.showMessageDialog(this, "Profil tidak ditemukan.");
        dispose();
    }
}



