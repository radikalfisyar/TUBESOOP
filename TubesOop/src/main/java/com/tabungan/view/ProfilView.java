    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed

                int confirm = JOptionPane.showConfirmDialog(null,
                        "Apakah Anda yakin ingin menghapus profil ini?",
                        "Konfirmasi Hapus",
                        JOptionPane.YES_NO_OPTION);

                if (confirm == JOptionPane.YES_OPTION) {
                    ProfilController profilController = new ProfilController();
                    if (profilController.hapusProfil(username)) {
                        JOptionPane.showMessageDialog(null, "Profil berhasil dihapus!");
                    } else {
                        JOptionPane.showMessageDialog(null, "Gagal menghapus profil.");
                    }
                }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
           String nama = txtNama.getText();
                String email = txtEmail.getText();
                String gender = (String) cmbGender.getSelectedItem();

                Profil profilBaru = new Profil(username, nama, email, gender);
                ProfilController profilController = new ProfilController();

                if (profilController.updateProfil(profilBaru)) {
                    JOptionPane.showMessageDialog(null, "Profil berhasil diperbarui!");
                    dispose(); // Tutup form profil setelah menyimpan
                } else {
                    JOptionPane.showMessageDialog(null, "Gagal memperbarui profil.");
                }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        // Ambil data dari input form
                String username = txtUsername.getText();
                String nama = txtNama.getText();
                String email = txtEmail.getText();
                String gender = (String) cmbGender.getSelectedItem();

                // Buat objek Profil
                Profil profil = new Profil(username, nama, email, gender);

                // Simpan profil menggunakan ProfilController
                boolean success = profilController.tambahProfil(profil);
                if (success) {
                    JOptionPane.showMessageDialog(null, "Profil berhasil disimpan!");
                } else {
                    JOptionPane.showMessageDialog(null, "Gagal menyimpan profil.", "Error", JOptionPane.ERROR_MESSAGE);
                }

    }//GEN-LAST:event_btnSimpanActionPerformed

